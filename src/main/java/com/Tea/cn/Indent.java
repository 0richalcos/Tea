package com.Tea.cn;

public class Indent {
	int id;
	String name;
	String size;
	String sugar;
	String oC;
	String dosings;
	int finish;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getSugar() {
		return sugar;
	}
	public void setSugar(String sugar) {
		this.sugar = sugar;
	}
	public String getoC() {
		return oC;
	}
	public void setoC(String oC) {
		this.oC = oC;
	}
	public String getDosings() {
		return dosings;
	}
	public void setDosings(String dosings) {
		this.dosings = dosings;
	}
	public int getFinish() {
		return finish;
	}
	public void setFinish(int finish) {
		this.finish = finish;
	}
	
	
}
